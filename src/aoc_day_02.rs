use std::str::Chars;

pub fn first_problem() {
    let lines: Vec<_> = get_input().split("\n").collect();
    let mut total: u128 = 0;

    for line in lines {

        let mut chars = get_chars(line);

        let opp = chars.next().unwrap();
        chars.next(); //to skip the empty space
        let me = chars.next().unwrap();
        let value: u128 = get_value(me) + win_value(opp, me);
        total += value;
    }
    println!("total value is {}", total);
}

pub fn second_problem() {
    let lines: Vec<_> = get_input().split("\n").collect();
    let mut total: u128 = 0;

    for line in lines {

        let mut chars = get_chars(line);
        let opp: Option<char> = chars.next();
        chars.next();
        let me: Option<char> = chars.next();
        let value: u128 = my_choice(opp.unwrap(), me.unwrap()) + win_score(me.unwrap());
        total += value;
    }
    println!("total value is {}", total);
}

fn get_chars(p0: &str) -> Chars {
    let chars = p0.trim().chars();
    return chars;
}


fn my_choice(opp: char, me: char) -> u128 {
    let mut value: u128;
    if me == 'Z' {
        value = get_value(opp) + 1;
    } else if me == 'Y' {
        value = get_value(opp);
    } else {
        value = get_value(opp) - 1;
    }

    if value > 3 {
        value = 1;
    }
    if value == 0 {
        value = 3;
    }

    return value;
}


fn win_score(c: char) -> u128 {
    return if c == 'X' {
        0
    } else if c == 'Y' {
        3
    } else {
        6
    };
}


fn get_value(c: char) -> u128 {
    if c == 'A' || c == 'X' {
        return 1;
    } else if c == 'B' || c == 'Y' {
        return 2;
    } else {
        return 3;
    }
}

fn win_value(opp: char, me: char) -> u128 {
    if get_value(opp) == get_value(me) {
        return 3;
    } else if get_value(opp) + 1 == (get_value(me)) ||
        (get_value(opp) == 3 && get_value(me) == 1) {
        return 6;
    }
    return 0;
}

